from __future__ import division, print_function, absolute_import
import re
import glob
import os.path as osp

from ..dataset import ImageDataset

class DukeMTMCreID_2(ImageDataset):
    """DukeMTMC-reID.

    Reference:
        - Ristani et al. Performance Measures and a Data Set for Multi-Target, Multi-Camera Tracking. ECCVW 2016.
        - Zheng et al. Unlabeled Samples Generated by GAN Improve the Person Re-identification Baseline in vitro. ICCV 2017.

    URL: `<https://github.com/layumi/DukeMTMC-reID_evaluation>`_
    
    Dataset statistics:
        - identities: 1404 (train + query).
        - images:16522 (train) + 2228 (query) + 17661 (gallery).
        - cameras: 8.
    """
    dataset_dir = 'dukemtmc-reid_2'
    # dataset_url = 'http://vision.cs.duke.edu/DukeMTMC/data/misc/DukeMTMC-reID.zip'

    def __init__(self, root='', **kwargs):
        self.root = osp.abspath(osp.expanduser(root))
        self.dataset_dir = osp.join(self.root, self.dataset_dir)
        # self.download_dataset(self.dataset_dir, self.dataset_url)
        self.train_dir = osp.join(
            self.dataset_dir, 'DukeMTMC-reID/bounding_box_train.txt'
        )
        self.query_dir = osp.join(self.dataset_dir, 'DukeMTMC-reID/query.txt')
        self.gallery_dir = osp.join(
            self.dataset_dir, 'DukeMTMC-reID/bounding_box_test.txt'
        )

        required_files = [
            self.dataset_dir, self.train_dir, self.query_dir, self.gallery_dir
        ]
        self.check_before_run(required_files)

        train = self.process_dir(self.train_dir, relabel=True)
        query = self.process_dir(self.query_dir, relabel=False)
        gallery = self.process_dir(self.gallery_dir, relabel=False)

        super(DukeMTMCreID_2, self).__init__(train, query, gallery, **kwargs)

    def process_dir(self, dir_path, relabel=False):
        data_read = []
        pid_container = set()

        # 读取并解析文件内容
        with open(dir_path, 'r') as file:
            for line in file:
                img_path, pid, camid = line.strip().split(', ')
                pid, camid = int(pid), int(camid)
                data_read.append((img_path, pid, camid))
                pid_container.add(pid)
        # 重新编码PID
        if relabel:
            pid2label = {pid: idx for idx, pid in enumerate(pid_container)}
            data_read = [(img_path, pid2label[pid], camid) for img_path, pid, camid in data_read]
        return data_read
